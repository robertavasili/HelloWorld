package CH9Inheritance;

public class Rectangle implements ShapeV1 {
    private double width;
    private double height;

    public Rectangle(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double area() {
        return width * height;
    }

    public double perimeter() {
        return 2 * (height + width);
    }
}
